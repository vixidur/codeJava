    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        String macansua = txtMaMay.getText();
        String ten = txtTenMay.getText();
        String hdh = txtHedieuhanh.getText();
        String hsx = txtHangSanXuat.getText();
        try {
            Connection ketnoi = Ketnoi.KetnoiSQL();
            String querry2 = "UPDATE tt_maytinh SET tenmay = '" + ten + "', hedieuhanh = '" + hdh + "', hangsx = '" + hsx + "' WHERE mamay = '" + macansua + "'";
            Statement stm = ketnoi.createStatement();
            stm.executeUpdate(querry2);

            String sql1 = "SELECT * FROM tt_maytinh";
            ResultSet rs = stm.executeQuery(sql1);
            DefaultTableModel dtm = (DefaultTableModel) tbl.getModel();
            dtm.setRowCount(0);
            while (rs.next()) {
                Object objList[] = {
                    rs.getInt(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4)
                };

                dtm.addRow(objList);
                tbl.setModel(dtm);
            }
        } catch (SQLException ex) {
            Logger.getLogger(QLMT.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnSuaActionPerformed


    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        try {
            String ma = txtMaMay.getText();
            String tenmay = txtTenMay.getText();
            String hdh = txtHedieuhanh.getText();
            String hsx = txtHangSanXuat.getText();
            Connection kn = Ketnoi.KetnoiSQL();
            String querry1 = "INSERT INTO tt_maytinh VALUES ('" + ma + "','" + tenmay + "', '" + hdh + "', '" + hsx + "')";
            Statement stm = kn.createStatement();
            stm.executeUpdate(querry1);

            String sql1 = "SELECT * FROM tt_maytinh";
            ResultSet rs = stm.executeQuery(sql1);
            DefaultTableModel dtm = (DefaultTableModel) tbl.getModel();
            dtm.setRowCount(0);
            while (rs.next()) {
                Object objList[] = {
                    rs.getInt(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4)
                };

                // add cac ban ghi vao mảng objectlist
                dtm.addRow(objList);
                tbl.setModel(dtm);
            }
        } catch (SQLException ex) {
            Logger.getLogger(QLMT.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void tblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMouseClicked
        int row = tbl.getSelectedRow();
        String ma = tbl.getValueAt(row, 0).toString();
        String tenmay = tbl.getValueAt(row, 1).toString();
        String hdh = tbl.getValueAt(row, 2).toString();
        String hsx = tbl.getValueAt(row, 3).toString();

        txtMaMay.setText(ma);
        txtTenMay.setText(tenmay);
        txtHedieuhanh.setText(hdh);
        txtHangSanXuat.setText(hsx);
    }//GEN-LAST:event_tblMouseClicked

